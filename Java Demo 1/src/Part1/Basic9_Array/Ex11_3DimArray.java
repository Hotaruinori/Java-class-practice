package Part1.Basic9_Array;

// 11.3D Array
public class Ex11_3DimArray
{
	public static void main(String[] args)
	{
		int[][][] ary = { { {0, 1, 2, 3}, {10, 11, 12, 13} },
				            { {100, 101, 102, 103}, {110, 111, 112, 113} },
				            { {200, 201, 202, 203}, {210, 211, 212, 213} },
			                { {300, 301, 302, 303}, {310, 311, 312, 313} },
				            { {400, 401, 402, 403}, {410, 411, 412, 413} },
				            { {500, 501, 502, 503}, {510, 511, 512, 513} } };
				            
		System.out.println("ary[1][0][2] = " + ary[1][0][2]);
		System.out.println("ary[3][1][0] = " + ary[3][1][0]);
		System.out.println("ary[5][0][3] = " + ary[5][0][3]);
	}
}


