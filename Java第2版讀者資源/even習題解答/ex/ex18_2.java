public class ex18_2 {
	public static void main(String[] args) {
		Integer a = Integer.valueOf("1010", 8);
		Integer b = Integer.valueOf("11111111", 8);
		Integer c = Integer.valueOf("100100", 8);		
		
		System.out.println("1010     = " + a);
		System.out.println("11111111 = " + b);
		System.out.println("100100   = " + c);
	}
}

