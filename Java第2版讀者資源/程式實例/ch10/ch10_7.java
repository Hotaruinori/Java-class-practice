public class ch10_7 {
	public static void main(String[] args) {
		int x1 = -30;							// 定義2個整數int
		int x2 = 50;
		System.out.println("-30的絕對值是 : " + Math.abs(x1));
		System.out.println(" 50的絕對值是 : " + Math.abs(x2));
		double y1 = -5.5;						// 定義2個雙倍精度浮點數double
		double y2 = 3.6;
		System.out.println("-5.5的絕對值是 : " + Math.abs(y1));
		System.out.println(" 3.6的絕對值是 : " + Math.abs(y2));
	}
}

