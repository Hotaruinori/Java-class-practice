public class ch12_12 {
	public static void main(String[] args) {
		String str1 = "Ming-Chi Institute of Technology";
		String str2 = "Ming-Chi University of Technology";
		System.out.println("str1轉換小寫前 = " + str1);
		System.out.println("str1轉換小寫後 = " + str1.toLowerCase());
		System.out.println("str2轉換大寫前 = " + str2);
		System.out.println("str2轉換大寫後 = " + str2.toUpperCase());
	}
}

